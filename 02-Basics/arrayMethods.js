

const numbers = ['One','Two','Three','Four','Five']

console.log(numbers);

// Removes first element
console.log(numbers.shift());
console.log(numbers);

// Adds an element at First Location
numbers.unshift(1)

console.log(numbers)

// Delete element from last Index.
numbers.pop()
console.log(numbers);

// push element into array at Last Indexs
numbers.push(24)
console.log(numbers);

numbers.splice(2,2,"splice")

console.log(numbers);




